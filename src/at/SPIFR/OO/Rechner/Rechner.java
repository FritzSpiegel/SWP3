package at.SPIFR.OO.Rechner;

public class Rechner {


    public Rechner() {
    }

    public float plus(float a, float b){
        float fResult = a + b;
        return fResult;
    }

    public float minus(float a,float b){
        float fResult = a - b;
        return fResult;
    }

    public float multiplizieren(float a,float b){
        float fResult = a * b;
        return fResult;
    }

    public float dividieren(float a,float b){
        float fResult = a / b;
        return fResult;
    }

}
